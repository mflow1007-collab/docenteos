// Códigos secundarios → código principal de su bloque (inverso de COMP_BLOQUES_MAP)
const CE_SECUNDARIO_A_PRINCIPAL = {
  "CE-LEI3": "CE-LEI2",
  "CE-LEI7": "CE-LEI4",
  "CE-LEI6": "CE-LEI5",
};

// Lookup inverso: descripción → código CE (para mapear texto libre a código)
const _DESC_A_CODIGO = Object.fromEntries(
  Object.entries(COMP_DESCRIPCIONES).map(([code, desc]) => [desc.toLowerCase(), code])
);

/**
 * Resuelve el índice de columna (ci) en el Registro dada un área y una competencia.
 * Acepta: código CE ("CE-LEI2"), código secundario ("CE-LEI3"), o descripción oficial.
 * Devuelve -1 si no puede resolver.
 */
export function resolverCI(area, competencia) {
  if (!area || !competencia) return -1;
  const codigos = COMP_CODIGOS[area] || [];
  if (!codigos.length) return -1;

  const comp = String(competencia).trim();

  // 1. Coincidencia directa con código primario
  const directa = codigos.indexOf(comp);
  if (directa >= 0) return directa;

  // 2. Código secundario → remap al primario
  const principal = CE_SECUNDARIO_A_PRINCIPAL[comp];
  if (principal) {
    const idx = codigos.indexOf(principal);
    if (idx >= 0) return idx;
  }

  // 3. Coincidencia por descripción oficial (exacta, case-insensitive)
  const codigo = _DESC_A_CODIGO[comp.toLowerCase()];
  if (codigo) {
    // El código encontrado puede ser primario o secundario
    const idx = codigos.indexOf(codigo);
    if (idx >= 0) return idx;
    const prin = CE_SECUNDARIO_A_PRINCIPAL[codigo];
    if (prin) return codigos.indexOf(prin);
  }

  // 4. Coincidencia parcial por primeros 40 caracteres de descripción
  const compPrefix = comp.toLowerCase().slice(0, 40);
  for (const [code, desc] of Object.entries(COMP_DESCRIPCIONES)) {
    if (desc.toLowerCase().startsWith(compPrefix)) {
      const idx = codigos.indexOf(code);
      if (idx >= 0) return idx;
      const prin = CE_SECUNDARIO_A_PRINCIPAL[code];
      if (prin) { const i2 = codigos.indexOf(prin); if (i2 >= 0) return i2; }
    }
  }

  return -1;
}

/**
 * Convierte una cadena de período a índice 0-based.
 * "Período 1" / "1" / 1 → 0
 * "Período 2" → 1, etc.
 */
export function resolverPI(periodoStr) {
  if (periodoStr === null || periodoStr === undefined) return -1;
  if (typeof periodoStr === "number") return periodoStr >= 1 ? periodoStr - 1 : -1;
  const match = String(periodoStr).match(/\d+/);
  if (!match) return -1;
  const n = parseInt(match[0], 10);
  return n >= 1 && n <= 4 ? n - 1 : -1;
}
