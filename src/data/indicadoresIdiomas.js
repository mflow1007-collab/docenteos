/**
 * Devuelve los datos curriculares oficiales (competenciaEspecifica, nivelMCERL,
 * indicadores seleccionados) para Inglés o Francés según el grado.
 *
 * Selección: 1 de Listening + 2 de Speaking (énfasis CLT) + 1 de Reading + 1 de Writing
 * = 5 indicadores representativos de las 4 habilidades.
 */
export const getCompetenciasIdiomas = (area, grado) => {
  const banco = area === "Francés" ? INDICADORES_FRANCES : INDICADORES_INGLES;

  // Normalizar el grado: "2do Secundaria" → "2do", "Segundo" → "2do", etc.
  const gradoNorm = normalizarGrado(grado);
  const datos = banco[gradoNorm] ?? banco["2do"];

  const h = datos.indicadoresPorHabilidad;

  return {
    nivelMCERL: datos.nivel,
    especifica: datos.competenciaEspecifica,
    indicadores: [
      h.listening[0],
      h.speaking[0],
      h.speaking[1] ?? h.speaking[0],
      h.reading[0],
      h.writing[0],
    ],
  };
};

// ─── Normalización de grado ───────────────────────────────────────────────────

const TABLA_GRADO = {
  "1ro": "1ro", "primero": "1ro", "first": "1ro", "1": "1ro",
  "2do": "2do", "segundo": "2do", "second": "2do", "2": "2do",
  "3ro": "3ro", "tercero": "3ro", "third": "3ro", "3": "3ro",
  "4to": "4to", "cuarto": "4to", "fourth": "4to", "4": "4to",
  "5to": "5to", "quinto": "5to", "fifth": "5to", "5": "5to",
  "6to": "6to", "sexto": "6to", "sixth": "6to", "6": "6to",
};

const normalizarGrado = (grado = "") => {
  const tok = String(grado).toLowerCase().trim().split(/\s+/)[0];
  return TABLA_GRADO[tok] ?? "2do";
};
